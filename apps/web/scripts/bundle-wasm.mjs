#!/usr/bin/env node
/**
 * Pre-build: embed the solver-wasm binary as a base64 string in a TS
 * module so it ships inside the JS bundle. Sidesteps Vercel's Function
 * packager filtering .wasm files out of .next/server/chunks/.
 *
 * Input : node_modules/@jayj1990/gto-today-solver-wasm/gto_today_solver_wasm_bg.wasm
 * Output: apps/web/src/lib/solver-wasm-bytes.ts
 *
 * Re-run automatically before `next build`. Also safe to call ad-hoc.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const __dirname = dirname(fileURLToPath(import.meta.url));
const APP_ROOT = resolve(__dirname, '..');

const require = createRequire(import.meta.url);
const pkgJsonPath = require.resolve('@jayj1990/gto-today-solver-wasm/package.json');
const wasmPath = resolve(dirname(pkgJsonPath), 'gto_today_solver_wasm_bg.wasm');
const pkgJson = JSON.parse(readFileSync(pkgJsonPath, 'utf8'));

if (!existsSync(wasmPath)) {
  throw new Error(`[bundle-wasm] binary not found at ${wasmPath}`);
}

const bytes = readFileSync(wasmPath);
const base64 = bytes.toString('base64');

const outDir = resolve(APP_ROOT, 'src', 'lib');
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });

const outPath = resolve(outDir, 'solver-wasm-bytes.ts');

// Single literal. tsc's AST walker stack-overflows on thousands of
// concatenated `'x' + 'y'` nodes, so no line-wrapping — one big
// string. The 388KB base64 is loaded once at module init and dropped
// once Uint8Array materialises.
const content = `// AUTO-GENERATED by scripts/bundle-wasm.mjs — do not edit.
// Source: @jayj1990/gto-today-solver-wasm@${pkgJson.version}
// Binary: gto_today_solver_wasm_bg.wasm (${bytes.length} bytes)

/* eslint-disable */
const BASE64 = '${base64}';

// wasm-bindgen's init() distinguishes Response/URL/Request instances
// at runtime; a Node Buffer subclass of Uint8Array occasionally falls
// through the check wrong and breaks module instantiation on cold
// starts. Materialise a plain Uint8Array so the type is unambiguous.
// Cost is a single 290KB copy at server boot — negligible.
const _buf = Buffer.from(BASE64, 'base64');
export const SOLVER_WASM_BYTES: Uint8Array = new Uint8Array(
  _buf.buffer,
  _buf.byteOffset,
  _buf.byteLength,
);

export const SOLVER_WASM_VERSION = '${pkgJson.version}';
`;

writeFileSync(outPath, content);
console.log(
  `[bundle-wasm] wrote ${outPath} (${bytes.length}B binary → ${base64.length}B base64)`,
);
